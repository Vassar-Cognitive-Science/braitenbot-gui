import { describe, it, expect } from 'vitest';
import { buildGraph } from '../graph';
import { generateSketch } from '../emitter';
import type { DiagramNode, DiagramConnection } from '../../types/diagram';

function makeSensor(overrides: Partial<DiagramNode> = {}): DiagramNode {
  return {
    id: 'sensor-1',
    type: 'sensor-analog',
    label: 'Sensor 1',
    x: 0,
    y: 0,
    arduinoPort: 'A0',
    ...overrides,
  };
}

function makeMotor(overrides: Partial<DiagramNode> = {}): DiagramNode {
  return {
    id: 'motor-left',
    type: 'motor',
    label: 'Left Motor',
    x: 0,
    y: 0,
    motorPinFwd: '5',
    motorPinRev: '6',
    ...overrides,
  };
}

function conn(c: Omit<DiagramConnection, 'transferMode' | 'transferPoints'> & Partial<Pick<DiagramConnection, 'transferMode' | 'transferPoints'>>): DiagramConnection {
  return {
    transferMode: 'linear',
    transferPoints: [{ x: 0, y: 0 }, { x: 1, y: 1 }],
    ...c,
  };
}

describe('generateSketch', () => {
  it('generates a minimal sketch with float signals', () => {
    const nodes: DiagramNode[] = [makeSensor(), makeMotor()];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'sensor-1', to: 'motor-left', weight: 0.8 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('Auto-generated by BraitenBot GUI');
    // Analog sensor normalized to 0–1
    expect(code).toContain('analogRead(SENSOR_sensor_1) / 1023.0');
    expect(code).toContain('float sig_sensor_1');
    expect(code).toContain('analogWrite');
    expect(code).toContain('0.8000');
    expect(code).toContain('delay(20)');
  });

  it('handles negative weight with reverse pin logic', () => {
    const nodes: DiagramNode[] = [makeSensor(), makeMotor()];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'sensor-1', to: 'motor-left', weight: -0.5 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('-0.5000');
    expect(code).toContain('if (input_motor_left >= 0)');
    expect(code).toContain('fabs(');
    expect(code).toContain('* 255.0');
  });

  it('generates threshold node with float comparison', () => {
    const thresh: DiagramNode = {
      id: 'thresh-1',
      type: 'compute-threshold',
      label: 'Threshold',
      x: 0,
      y: 0,
      threshold: 0.7,
    };
    const nodes: DiagramNode[] = [makeSensor(), thresh, makeMotor()];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'sensor-1', to: 'thresh-1', weight: 1 }),
      conn({ id: 'c2', from: 'thresh-1', to: 'motor-left', weight: 1 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('> 0.7000');
    expect(code).toContain('? 1.0 : 0.0');
    expect(code).toContain('float sig_thresh_1');
  });

  it('generates delay node as a float ring buffer', () => {
    const delayNode: DiagramNode = {
      id: 'delay-1',
      type: 'compute-delay',
      label: 'Delay',
      x: 0,
      y: 0,
      delayMs: 200,
    };
    const nodes: DiagramNode[] = [makeSensor(), delayNode, makeMotor()];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'sensor-1', to: 'delay-1', weight: 1 }),
      conn({ id: 'c2', from: 'delay-1', to: 'motor-left', weight: 1 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('BUF_SIZE = 10');
    expect(code).toContain('static float sig_delay_1_buf');
    expect(code).toContain('float sig_delay_1 =');
  });

  it('generates digital sensor as float', () => {
    const nodes: DiagramNode[] = [
      makeSensor({ id: 'dig-1', type: 'sensor-digital', arduinoPort: '2' }),
      makeMotor(),
    ];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'dig-1', to: 'motor-left', weight: 1 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('(float)digitalRead');
    expect(code).toContain('float sig_dig_1');
    expect(code).toContain('pinMode');
  });

  it('includes Wire.h for I2C sensors', () => {
    const nodes: DiagramNode[] = [
      makeSensor({ id: 'i2c-1', type: 'sensor-i2c', label: 'I2C' }),
      makeMotor(),
    ];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'i2c-1', to: 'motor-left', weight: 1 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('#include <Wire.h>');
    expect(code).toContain('Wire.begin()');
    expect(code).toContain('float sig_i2c_1 = 0.0');
  });

  it('generates comparator node with float output', () => {
    const comp: DiagramNode = {
      id: 'comp-1',
      type: 'compute-comparator',
      label: 'Comparator',
      x: 0,
      y: 0,
      comparatorOp: '>=',
    };
    const s2 = makeSensor({ id: 'sensor-2', label: 'Sensor 2', arduinoPort: 'A1' });
    const nodes: DiagramNode[] = [makeSensor(), s2, comp, makeMotor()];
    const connections: DiagramConnection[] = [
      conn({ id: 'c1', from: 'sensor-1', to: 'comp-1', weight: 1 }),
      conn({ id: 'c2', from: 'sensor-2', to: 'comp-1', weight: 1 }),
      conn({ id: 'c3', from: 'comp-1', to: 'motor-left', weight: 1 }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('>=');
    expect(code).toContain('? 1.0 : 0.0');
  });

  it('generates non-linear transfer function', () => {
    const nodes: DiagramNode[] = [makeSensor(), makeMotor()];
    const connections: DiagramConnection[] = [
      conn({
        id: 'c1',
        from: 'sensor-1',
        to: 'motor-left',
        weight: 1,
        transferMode: 'nonlinear',
        transferPoints: [
          { x: 0, y: 0 },
          { x: 0.5, y: 0.8 },
          { x: 1, y: 1 },
        ],
      }),
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('float transfer_');
    expect(code).toContain('float x)');
    expect(code).toContain('transfer_sensor_1_motor_left_0');
    // No * 1023.0 scaling — output is already in -1 to 1 signal domain
    expect(code).not.toContain('* 1023.0');
  });
});

import { describe, it, expect } from 'vitest';
import { buildGraph } from '../graph';
import { generateSketch } from '../emitter';
import type { DiagramNode, DiagramConnection } from '../../types/diagram';

function makeSensor(overrides: Partial<DiagramNode> = {}): DiagramNode {
  return {
    id: 'sensor-1',
    type: 'sensor-analog',
    label: 'Sensor 1',
    x: 0,
    y: 0,
    arduinoPort: 'A0',
    ...overrides,
  };
}

function makeMotor(overrides: Partial<DiagramNode> = {}): DiagramNode {
  return {
    id: 'motor-left',
    type: 'motor',
    label: 'Left Motor',
    x: 0,
    y: 0,
    motorPinFwd: '5',
    motorPinRev: '6',
    ...overrides,
  };
}

describe('generateSketch', () => {
  it('generates a minimal sketch for sensor → motor', () => {
    const nodes: DiagramNode[] = [makeSensor(), makeMotor()];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'sensor-1', to: 'motor-left', weight: 0.8 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('Auto-generated by BraitenBot GUI');
    expect(code).toContain('analogRead');
    expect(code).toContain('analogWrite');
    expect(code).toContain('SENSOR_sensor_1');
    expect(code).toContain('MOTOR_motor_left_FWD');
    expect(code).toContain('MOTOR_motor_left_REV');
    expect(code).toContain('0.8000');
    expect(code).toContain('delay(20)');
  });

  it('handles negative weight with reverse pin logic', () => {
    const nodes: DiagramNode[] = [makeSensor(), makeMotor()];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'sensor-1', to: 'motor-left', weight: -0.5 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('-0.5000');
    expect(code).toContain('if (input_motor_left >= 0)');
    expect(code).toContain('MOTOR_motor_left_REV');
  });

  it('generates threshold node code', () => {
    const thresh: DiagramNode = {
      id: 'thresh-1',
      type: 'compute-threshold',
      label: 'Threshold',
      x: 0,
      y: 0,
      threshold: 700,
    };
    const nodes: DiagramNode[] = [makeSensor(), thresh, makeMotor()];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'sensor-1', to: 'thresh-1', weight: 1 },
      { id: 'c2', from: 'thresh-1', to: 'motor-left', weight: 1 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('> 700');
    expect(code).toContain('? 1023 : 0');
  });

  it('generates delay node as a ring buffer', () => {
    const delayNode: DiagramNode = {
      id: 'delay-1',
      type: 'compute-delay',
      label: 'Delay',
      x: 0,
      y: 0,
      delayMs: 200,
    };
    const nodes: DiagramNode[] = [makeSensor(), delayNode, makeMotor()];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'sensor-1', to: 'delay-1', weight: 1 },
      { id: 'c2', from: 'delay-1', to: 'motor-left', weight: 1 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    // 200ms / 20ms loop = 10 slots
    expect(code).toContain('BUF_SIZE = 10');
    expect(code).toContain('_buf[10]');
    expect(code).toContain('% sig_delay_1_BUF_SIZE');
    expect(code).toContain('static');
  });

  it('generates digital sensor with normalization', () => {
    const nodes: DiagramNode[] = [
      makeSensor({ id: 'dig-1', type: 'sensor-digital', arduinoPort: '2' }),
      makeMotor(),
    ];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'dig-1', to: 'motor-left', weight: 1 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('digitalRead');
    expect(code).toContain('* 1023');
    expect(code).toContain('pinMode');
  });

  it('includes Wire.h for I2C sensors', () => {
    const nodes: DiagramNode[] = [
      makeSensor({ id: 'i2c-1', type: 'sensor-i2c', label: 'I2C' }),
      makeMotor(),
    ];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'i2c-1', to: 'motor-left', weight: 1 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('#include <Wire.h>');
    expect(code).toContain('Wire.begin()');
    expect(code).toContain('TODO');
  });

  it('generates comparator node code', () => {
    const comp: DiagramNode = {
      id: 'comp-1',
      type: 'compute-comparator',
      label: 'Comparator',
      x: 0,
      y: 0,
      comparatorOp: '>=',
    };
    const s2 = makeSensor({ id: 'sensor-2', label: 'Sensor 2', arduinoPort: 'A1' });
    const nodes: DiagramNode[] = [makeSensor(), s2, comp, makeMotor()];
    const connections: DiagramConnection[] = [
      { id: 'c1', from: 'sensor-1', to: 'comp-1', weight: 1 },
      { id: 'c2', from: 'sensor-2', to: 'comp-1', weight: 1 },
      { id: 'c3', from: 'comp-1', to: 'motor-left', weight: 1 },
    ];
    const graph = buildGraph(nodes, connections);
    const code = generateSketch(graph);

    expect(code).toContain('>=');
    expect(code).toContain('? 1023 : 0');
  });
});
